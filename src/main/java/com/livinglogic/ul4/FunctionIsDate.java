package com.livinglogic.ul4;

import java.time.LocalDate;

public class FunctionIsDate extends Function
{
	@Override
	public String getNameUL4()
	{
		return "isdate";
	}

	private static final Signature signature = new Signature().addPositionalOnly("obj");

	@Override
	public Signature getSignature()
	{
		return signature;
	}

	@Override
	public Object evaluate(BoundArguments args)
	{
		return call(args.get(0));
	}

	public static boolean call(Object obj)
	{
		return Date_.type.instanceCheck(obj);
	}

	public static final Function function = new FunctionIsDate();
}
